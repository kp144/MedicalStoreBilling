    public msbsUser getCodeSelectedRow() {
        int index = tblUsermanager.getSelectedRow();
        int userCode = (int) Integer.valueOf(String.valueOf(tblUsermanager.getValueAt(index, 0)));
        user = userMn.getUserByCode(userCode);
        if (user != null) {
            return user;
        }
        return null;
    }

    public void getUser() {
        String uName;
        String uType;
        allUser = new Vector<msbsUser>();
        allUser = userMn.getAllUser();

        if (txtNameFilter.getText().trim().equals(null) || txtNameFilter.getText().trim().equals("")) {
            uName = "";
        } else {
            uName = txtNameFilter.getText().trim().toLowerCase();
        }
        if (cbxUserType.getSelectedIndex() == 0) {
            uType = "";
        } else if (cbxUserType.getSelectedIndex() == 1) {
            uType = "Manager";
        } else if (cbxUserType.getSelectedIndex() == 2) {
            uType = "Seller";
        } else {
            uType = "Accountant";
        }
        model = new DefaultTableModel();
        model.addColumn("Code");
        model.addColumn("Name Login");
        model.addColumn("Full Name");
        model.addColumn("Type");
        model.addColumn("Phone");
        model.addColumn("Address");
        model.addColumn("Email");
        model.addColumn("Active");
        totalUser = 0;

        for (int i = 0; i < allUser.size(); i++) {
            user = (msbsUser) allUser.get(i);
            Vector newData = new Vector();
            if (user.getFullName().toLowerCase().matches(".*" + uName.toLowerCase() + ".*") && userMn.getUserTypeName(user.getUserTypeCode()).toLowerCase().matches(".*" + uType.toLowerCase() + ".*")) {
                newData.addElement(user.getUserCode());
                newData.addElement(user.getNameLogin());
                newData.addElement(user.getFullName());
                newData.addElement(userMn.getUserTypeName(user.getUserTypeCode()));
                newData.addElement(user.getUserPhone());
                newData.addElement(user.getUserAddress());
                newData.addElement(user.getUserEmail());
                newData.addElement(user.getUserActive());
                model.addRow(newData);
                totalUser++;
            }


        }
        tblUsermanager.setModel(model);
        tableColumnl = tblUsermanager.getColumnModel().getColumn(0);
        tableColumnl.setPreferredWidth(0);
        tableColumnl.setMinWidth(0);
        tableColumnl.setMaxWidth(0);
        lblTotalUser.setText(String.valueOf(totalUser));
    }

