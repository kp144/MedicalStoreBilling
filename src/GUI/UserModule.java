    private void btEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditActionPerformed
        // TODO add your handling code here:
        if (tblUsermanager.getSelectedRowCount() == 1) {
            UpdateUserForm updateUser = new UpdateUserForm(this.frmParent, true, getCodeSelectedRow());
            updateUser.setLocationRelativeTo(this);
            updateUser.setVisible(true);
            getUser();
        } else if (tblUsermanager.getSelectedRowCount() > 1) {
            JOptionPane.showMessageDialog(this, "Do not select more one User", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else if (tblUsermanager.getSelectedRowCount() > 1) {
            JOptionPane.showMessageDialog(this, "Do not select more one User", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Please Select a User", "WARNING", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btEditActionPerformed

    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        // TODO add your handling code here:
        msbsUser tempUser = new msbsUser();

        if (tblUsermanager.getSelectedRowCount() == 1) {
            tempUser = getCodeSelectedRow();
            int result;
            if (Login.userLogged.getUserCode() != tempUser.getUserCode()) {
                result = JOptionPane.showConfirmDialog(frmParent, "Are you sure delete this User? ");
                if (result == JOptionPane.YES_OPTION) {
                    if (userMn.deleteUser(tempUser.getUserCode())) {
                        JOptionPane.showMessageDialog(frmParent, "Delete successfull! ");
                    }else{
                        JOptionPane.showMessageDialog(this, "Cannot Delete this User !", "DELETE FAILED", JOptionPane.ERROR_MESSAGE);
                    }
                    getUser();
                } else if (result == JOptionPane.NO_OPTION) {
                    getUser();
                }
            } else {
                JOptionPane.showMessageDialog(this, "This user is logging", "WARNING", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Do not select more than one User", "WARNING", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btDeleteActionPerformed

